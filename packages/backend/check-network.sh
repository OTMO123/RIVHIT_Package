#!/bin/bash

echo "=== Проверка сетевых настроек ==="
echo ""

echo "1. Ваши сетевые интерфейсы:"
ifconfig | grep -A 1 "inet 192.168" || ifconfig | grep -A 1 "inet "

echo ""
echo "2. Таблица маршрутизации:"
netstat -rn | grep "192.168"

echo ""
echo "3. ARP таблица (видимые устройства в сети):"
arp -a | grep "192.168.14"

echo ""
echo "=== Рекомендации ==="
echo ""
echo "Если ваш компьютер НЕ в подсети 192.168.14.x, то:"
echo "1. Принтер и компьютер должны быть в одной сети"
echo "2. Измените IP принтера на вашу подсеть (например, 192.168.1.x)"
echo "3. Или подключите компьютер к той же сети, что и принтер"
echo ""
echo "Текущие настройки принтера:"
echo "  IP: 192.168.14.200"
echo "  Subnet: 255.255.255.0"
echo "  Gateway: 192.168.14.1"